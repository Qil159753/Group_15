#include <iostream>
using namespace std;

int main() {
  
  	// while loop to print numbers from 1 to 5
    int i = 0;//variable declaration 'i' as integer and has 0 value
    while (i < 5) {//While loop condition as if 'i' still less than 5
        cout << "Hi  => i = " << i << endl;//Show "Hi = " 'i'
        i++;//+1 to 'i'
    }

    return 0;
}