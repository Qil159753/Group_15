#include <iostream>
using namespace std;

int main() {

  	// for loop to print "Hi" 5 times
    for (int i = 0; i < 5; i++) {//For loop "i" begin at 0 then add up +1 every time for each loop until "i" is more than or equal to 5
      	cout << "Hi => i = " << i << endl;//show "Hi = " and 'i' and then repeat the loop
    }
  
    return 0;
}